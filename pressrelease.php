<h3 class="text--center text--grey">Press Release</h3>
<h1 class="text--center text--red">People with HIV are living longer but still face many challenges</h1>
<hr>
<p><span style="text-decoration:underline">Brussels, 7 December 2016:</span> The substantial increase in life expectancy for people living with HIV
(PLHIV) is a hugely important achievement. However, PLHIV continue to face significant healthrelated
challenges which are in urgent need of attention from policy makers and the wider health
community.</p>
<p>A new initiative, ‘HIV Outcomes: Beyond Viral Suppression’, today launched a campaign to highlight
and address these challenges, which include co-existing physical and mental health conditions (that
PLHIV are at higher risk of developing), and social inclusion – which continues to be undermined by
stigma and discrimination.</p>
<p>The initiative, which features high profile patient representatives, clinicians, academics, and industry,
wants health systems and HIV policy frameworks to take account of these diverse challenges, so that
PLHIV gain access to the necessary health and support services in order to live long and well.</p>
<p>‘HIV Outcomes: Beyond Viral Suppression’ seeks to complement other HIV initiatives focusing on
prevention, diagnosis and access to effective care, where there are global targets for the HIV
response but much work remains to be done. The initiative also builds upon the WHO Global Health
Sector Strategy on HIV, 2016-2021, which highlights the need for PLHIV to have access to chronic
care, but does not provide specific objectives or targets in that area.</p>
<p>Speaking ahead of the launch event in Brussels today, Nikos Dedes, Co-Chair of the HIV Outcomes:
Beyond Viral Suppression Steering Group, said: <em>“Current HIV policy was not conceived with ageing in
mind, but living long term with HIV creates its own specific needs. The policy environment must adapt
to recognise, measure and address these needs.”</em></p>
<p>Professor Jeffrey Lazarus of ISGlobal, Hospital Clínic at the University of Barcelona, is leading a study
group composed of HIV and health system experts in order to assess the performance of health
systems in addressing the challenges highlighted in this initiative. Lazarus presented an interim
report at today’s launch event.</p>
<p>The campaign comes at a watershed moment for HIV/AIDS policy with the EU’s Action Plan set to
expire at the end of this year. The Initiative seeks to encourage the European Commission to adopt a
new integrated framework on HIV, viral hepatitis and tuberculosis that can provide a coordinated
response to these threats. The Steering Group also wants to see the European monitoring framework
on HIV (based on the 2004 Dublin Declaration) expanded to reflect new and ongoing health and
social needs of PLHIV.</p>
