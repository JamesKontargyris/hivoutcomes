<h4>Notes for editors</h4>
<p>The Beyond Viral Suppression initiative arises out of a shared recognition among leading HIV experts that there are crucially important issues relating to the health and social inclusion of PLHIV that have to date received insufficient attention from policy makers and healthcare providers, and which must now form part of the HIV response. The steering group is co-chaired by: <strong>Nikos Dedes</strong>, the Founder of Positive Voice (the Greek association for PLHIV) and a Board member of the European AIDS Treatment Group (EATG); Professor <strong>Jane Anderson</strong> of Homerton University Hospital NHS Foundation Trust in London; and Professor <strong>Jeffrey Lazarus</strong> of ISGlobal, Hospital Clínic at the University of Barcelona, and CHIP, Rigshospitalet, University of Copenhagen. The initiative is enabled by sponsorship provided by Gilead Sciences and ViiV Healthcare.</p>

