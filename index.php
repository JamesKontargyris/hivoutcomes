<h1 class="text--center text--red">Our Initiative</h1>
<hr>
<p>"HIV Outcomes: Beyond viral suppression" arises out of a shared recognition among leading HIV experts that there are crucially important issues relating to the health and social inclusion of people living with HIV (PLHIV) that have to date received insufficient attention from policy makers and healthcare providers, and which must now form part of our HIV response.</p>
 
<p>In an era when ageing populations and health system sustainability are central challenges for all European countries, the initiative also aims to inform debates about cost-effective strategies for co-morbidity prevention and management, whilst ensuring patient-centred healthcare delivery. Our recommendations should therefore be of high relevance both to policy makers and the wider health community.</p>
 
